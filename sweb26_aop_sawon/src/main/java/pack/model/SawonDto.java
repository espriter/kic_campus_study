package pack.model;

public class SawonDto {
	private String sawon_no, sawon_name, sawon_jik, sawon_gen, buser_name;

	public String getSawon_no() {
		return sawon_no;
	}

	public void setSawon_no(String sawon_no) {
		this.sawon_no = sawon_no;
	}

	public String getSawon_name() {
		return sawon_name;
	}

	public void setSawon_name(String sawon_name) {
		this.sawon_name = sawon_name;
	}

	public String getSawon_jik() {
		return sawon_jik;
	}

	public void setSawon_jik(String sawon_jik) {
		this.sawon_jik = sawon_jik;
	}

	public String getSawon_gen() {
		return sawon_gen;
	}

	public void setSawon_gen(String sawon_gen) {
		this.sawon_gen = sawon_gen;
	}

	public String getBuser_name() {
		return buser_name;
	}

	public void setBuser_name(String buser_name) {
		this.buser_name = buser_name;
	}
	
	
}
